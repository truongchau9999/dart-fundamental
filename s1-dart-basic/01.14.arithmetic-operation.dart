void main(List<String> args) {
  int a = 5;
  int b = 2;
  //cong
  print(a + b);

  print('---------------------------');

  //tru
  print(a - b);

  print('---------------------------');

  //nhan
  print(a * b);

  print('---------------------------');

  //chia (double)
  print(a / b); //2.5

  print('---------------------------');

  //chia (int)
  print(a ~/ b); //2

  print('---------------------------');

  //chia lay du
  print(a % b); //1

  print('---------------------------');
}
